# Операции сдвига влево << и сдвига вправо >>
x = 5  # сдвиг влево на 3 знака, умножение на 2**3 = 8
y = x << 3  # y = x*2**3 = 40
print('x = ', x)
print('y = x<<3 = ', y)

x = 25
y = x >> 2  # y = 6
print('x = ', x)
print('y = x>>2 = ', y)

# Для отрицательных чисел
x = -10
y = x << 1  # y = -20
print('x = ', x)
print('y = x<<1 = ', y)

x = -100
y = x >> 3  # y = -13
print('x = ', x)
print('y = x>>3 = ', y)
